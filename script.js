// ARITHEMATIC OPERATOR
let a = 7;
let b = 5;

console.log("a = ", a, "& b = ", b);
console.log("a + b = ", a + b);
console.log("a + b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b); 
console.log("a % b = ", a % b);
console.log("a ** b =", a ** b);

// UNARY OPERATOR
a++;
console.log("a =", a);

b--;
console.log("b =", b);